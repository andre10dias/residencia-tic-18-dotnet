// Operadores aritméticos

int x = 10;
int y = 3;

int soma = x + y;
System.Console.WriteLine(x + " + " + y + " = " + soma);

int subtracao = x - y;
System.Console.WriteLine(x + " - " + y + " = " + subtracao);

int divisao = x / y;
System.Console.WriteLine(x + " / " + y + " = " + divisao);

int multiplicacao = x * y;
System.Console.WriteLine(x + " * " + y + " = " + multiplicacao);
